package fr.arezzo.designer.palette;

/**
 * Category helper class is used for the category names inside the palette
 *
 * @author akram.sahbi@esprit.tn
 */
public class Category {

    //name of a category in the palette
    private String name;

    public Category() {
    }

    /**
     * 
     * @return the name of a category
     */
    public String getName() {
        return name;
    }

    /**
     * 
     * @param name the name of a category
     */
    public void setName(String name) {
        this.name = name;
    }
}
